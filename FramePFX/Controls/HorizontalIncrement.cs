namespace FramePFX.Controls {
    public enum HorizontalIncrement {
        /// <summary>
        /// Decrement the value when dragged left, increment when dragged right (default)
        /// </summary>
        LeftDecrRightIncr,
        /// <summary>
        /// Increment the value when dragged left, decrement when dragged right
        /// </summary>
        LeftIncrRightDecr
    }
}