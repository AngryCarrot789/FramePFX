namespace FramePFX.Controls
{
    public enum VerticalIncrement
    {
        /// <summary>
        /// Decrement the value when dragged up, and increment when dragged down (default)
        /// </summary>
        UpDecrDownIncr,

        /// <summary>
        /// Increment the value when dragged up, and decrement when dragged down
        /// </summary>
        UpIncrDownDecr
    }
}